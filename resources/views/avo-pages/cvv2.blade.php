<p><font face="Arial, Helvetica, sans-serif" size="2"><b>What is CVV2?</b><br>
CVV2 is an authentication mechanism created by card companies to help reduce
fraud with online transactions. It requires the card holder to have the card
physically in hand in order to enter three or four digits found printed on the
card itself.<br>
<br>
</font><font face="Arial, Helvetica, sans-serif" size="2"><b>Where do I find
the numbers?</b><br>
