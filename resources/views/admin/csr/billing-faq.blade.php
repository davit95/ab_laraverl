signed up for those extra features, our system or our customer service representatives disclosed that they would
be charged, but in fact we did not charge for them.</p>
</div>
<div id="quest">
<h3>Will I be charged for those extra features?</h3>
</div>
<div id="ans">
<p>Customers who added features prior to 2013 will not be billed for those charges from before 2013, but starting
in the first billing period in 2013 we will begin charging for those features.</p>
</div>
<div id="quest">
<h3>What are the fees and taxes?</h3>
</div>
<div id="ans">
<p>Similar to charges for the added features, in our "beta" phase, we did not charge our customers taxes and fees
